package com.yijiajiao.server.bean.post;

public class SmallQuestionBean {
	private int sort;
	private String smallQuestionId;
	private String type;
	private String answer;
	private int score;
	public int getSort() {
		return sort;
	}
	public void setSort(int sort) {
		this.sort = sort;
	}
	public String getSmallQuestionId() {
		return smallQuestionId;
	}
	public void setSmallQuestionId(String smallQuestionId) {
		this.smallQuestionId = smallQuestionId;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getAnswer() {
		return answer;
	}
	public void setAnswer(String answer) {
		this.answer = answer;
	}
	public int getScore() {
		return score;
	}
	public void setScore(int score) {
		this.score = score;
	}
	
}
